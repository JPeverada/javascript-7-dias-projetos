//Array Join 

const names = ['João', 'Maria', 'Pedro', 'Ana'];
console.log(names); // Exibe: ['João', 'Maria', 'Pedro', 'Ana']

const joinedNames = names.join(', '); // Junta os elementos do array em uma string,
console.log(joinedNames); // Exibe: "João, Maria, Pedro, Ana"


names.sort(); // Ordena os elementos do array em ordem alfabética
console.log(names); // Exibe: ['Ana', 'João', 'Maria', 'Pedro']

names.reverse(); // Inverte a ordem dos elementos do array
console.log(names); // Exibe: ['Pedro', 'Maria', 'João', '