//For loop
//Os loops são úteis se você quiser executar o mesmo código repetidamente, cada vez com um valor diferente.
//for (variavel de controle; condição; incremento) 
//A variável de controle é inicializada antes do loop começar. A condição é avaliada antes de cada iteração do loop. Se a condição for verdadeira, o bloco de código dentro do loop será executado. O incremento é executado após cada iteração do loop.
//Enquanto a condição for verdadeira, o loop continuará a ser executado. Se a condição for falsa, o loop será encerrado


for (let i = 0; i < 10; i++) {
    console.log('Número: '+ i);
}