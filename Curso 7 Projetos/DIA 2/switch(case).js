//Switch CASE

let aeroporto = 'MCO'

switch (aeroporto) {
    case 'MCO':
        console.log('Orlando');
        break;
    case 'LAX':
        console.log('Los Angeles');
        break;
    case 'JFK':
        console.log('New York');
        break;
    case 'SFO':
        console.log('San Francisco');
        break;
    default:
        console.log('Aeroporto não encontrado');
}