//While loop
//Os loops são úteis se você quiser executar o mesmo código repetidamente, cada vez com um valor diferente. 
//while (condição)
//A condição é avaliada antes de cada iteração do loop. Se a condição for verdadeira, o bloco de código dentro do loop será executado. Se a condição for falsa, o loop será encerrado.
//Enquanto a condição for verdadeira, o loop continuará a ser executado. Se a condição for falsa, o loop será encerrado.

let i = 0;
while (i < 10) {
    console.log(i);
    i++;
}