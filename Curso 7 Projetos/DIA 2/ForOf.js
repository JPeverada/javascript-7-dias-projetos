//For of
// O loop for...of é usado para iterar sobre objetos iteráveis, como arrays, strings, mapas, conjuntos e outros objetos que implementam o protocolo de iteração. Ele percorre os valores dos elementos do objeto iterável, permitindo que você acesse cada valor diretamente.

const friends = ['Alice', 'Bob', 'Charlie'];
for (let friend of friends) {
    console.log(friend);
}